// round off
	function round() {
			var a = document.getElementById('a-value').value;
			window.alert(Math.round(a));
		}

// squareroot of a
	function absolute() {
			var a = document.getElementById('a-value').value;
			window.alert(Math.sqrt(a).toFixed(2));
		}

// absolute value
	function squareRoot() {
			var a = document.getElementById('a-value').value;
			window.alert(Math.abs(a).toFixed(2));
		}

// a squared
	function power() {
			var a = document.getElementById('a-value').value;
			window.alert(Math.pow(a, 2).toFixed(2));
		}
